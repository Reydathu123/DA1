    public void setDiaChiFromString(String diaChiStr) {
        // Ví dụ chuỗi "123 ABC, Phường 5, Quận 10, TP.HCM"
        String[] parts = diaChiStr.split(",");
        if (parts.length >= 4) {
            txt_diachicuthe.setText(parts[0].trim());
            txt_phuongxa.setText(parts[1].trim());
            txt_quanhuyen.setText(parts[2].trim());
            txt_tp.setText(parts[3].trim());
        }
    }

    public diachikhachhang getFormDiaChi() {
        diachikhachhang dc = new diachikhachhang();
        dc.setDiachicuthe(txt_diachicuthe.getText().trim());
        dc.setPhuongxa((String) txt_phuongxa.getText().trim());
        dc.setQuanhuyen((String) txt_quanhuyen.getText().trim());
        dc.setTinhthanh((String) txt_tp.getText().trim());
        return dc;
    }


