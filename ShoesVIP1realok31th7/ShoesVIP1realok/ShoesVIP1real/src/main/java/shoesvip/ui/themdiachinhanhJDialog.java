    private void btn_addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_addActionPerformed
        // TODO add your handling code here:
        if (!XDialog.confirm("Bạn có muốn thêm không")) {
            return;
        }

        diachikhachhang dc = this.getFormDiaChi(); // bạn cần viết thêm hàm này giống getformChatLieu...
        diachikhachhangDAO dcDAO = new diachikhachhangDAOImpl();
        // Kiểm tra rỗng
        if (txt_diachicuthe.getText().trim().isEmpty()
                || txt_phuongxa.getText().trim() == null
                || txt_quanhuyen.getText().trim() == null
                || txt_tp.getText().trim() == null) {
            XDialog.alert("Vui lòng nhập đầy đủ thông tin địa chỉ");
            return;
        }

        // Kiểm tra trùng
        for (diachikhachhang d : dcDAO.findAll()) {
            if (d.getDiachicuthe().equalsIgnoreCase(txt_diachicuthe.getText().trim())
                    && d.getPhuongxa().equalsIgnoreCase((String) txt_phuongxa.getText().trim())
                    && d.getQuanhuyen().equalsIgnoreCase((String) txt_quanhuyen.getText().trim())
                    && d.getTinhthanh().equalsIgnoreCase((String) txt_tp.getText().trim())) {
                XDialog.alert("Địa chỉ đã tồn tại");
                return;
            }
        }

        // Thêm
        dcDAO.add(dc);
    XDialog.alert("Thêm địa chỉ thành công");
    this.diaChiVuaThem = dc; // Gán địa chỉ vừa thêm
    this.submitted = true;
    this.dispose();
    }//GEN-LAST:event_btn_addActionPerformed

