    private static final int VIEW_COL_MA = 1;       // Cột mã SPCT trên UI (view index)
    private static final int VIEW_COL_ACTION = 16;   // Cột checkbox/Hành động trên UI (view index) -> sửa nếu khác

    @Override
    public void DownloadQR() {
        // Đặt lại cho đúng index cột của bạn

        try {
            // 1. Lấy danh sách mã cần tải (ưu tiên các dòng được tích)
            List<String> maCanTai = getMaSPCTCanTai();
            
            if (maCanTai.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Không có sản phẩm nào để tải (bảng rỗng?).");
                return;
            }

            // 2. Chọn thư mục đích
            JFileChooser chooser = new JFileChooser();
            chooser.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
            int result = chooser.showSaveDialog(this);
            if (result != JFileChooser.APPROVE_OPTION) {
                return; // người dùng cancel
            }
            File saveDir = chooser.getSelectedFile();

            // 3. Thư mục gốc chứa file QR (CHỈNH CHO ĐÚNG!)
            File qrRoot = new File("D:/qr"); // TODO: thay bằng thư mục thật hoặc đọc từ config

            int count = 0;
            List<String> missing = new ArrayList<>();

            // 4. Copy từng file
            for (String maSPCT : maCanTai) {
                if (maSPCT == null || maSPCT.isBlank()) {
                    continue;
                }
                String qrFileName = maSPCT.trim() + ".png";
                File qrFile = new File(qrRoot, qrFileName);
                
                if (!qrFile.exists()) {
                    missing.add(maSPCT);
                    continue;
                }
                
                try {
                    java.nio.file.Files.copy(
                            qrFile.toPath(),
                            new File(saveDir, qrFileName).toPath(),
                            java.nio.file.StandardCopyOption.REPLACE_EXISTING
                    );
                    count++;
                } catch (IOException io) {
                    missing.add(maSPCT + " (lỗi copy)");
                }
            }

            // 5. Thông báo kết quả
            if (missing.isEmpty()) {
                JOptionPane.showMessageDialog(this,
                        "Đã tải " + count + " mã QR vào: " + saveDir.getAbsolutePath());
            } else {
                JOptionPane.showMessageDialog(this,
                        "Đã tải " + count + " mã QR (thiếu " + missing.size() + "):\n"
                        + String.join(", ", missing));
            }
            
        } catch (Exception ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "Lỗi khi tải QR: " + ex.getMessage());
        }
    }

    /*  **
 * Lấy danh sách mã SPCT cần tải.
 * - Nếu có ít nhất 1 dòng được tích ở cột Hành động -> chỉ lấy các dòng đó.
 * - Nếu không tích dòng nào -> lấy tất cả các dòng ĐANG HIỂN THỊ trong bảng (đã lọc).
     */
    private List<String> getMaSPCTCanTai() {
        List<String> maList = new ArrayList<>();

        // Lấy model và quy đổi cột view -> model
        DefaultTableModel model = (DefaultTableModel) tbl_sanPhamCT.getModel();
        int modelColMa = tbl_sanPhamCT.convertColumnIndexToModel(VIEW_COL_MA);
        int modelColAction = tbl_sanPhamCT.convertColumnIndexToModel(VIEW_COL_ACTION);

        // 1. Quét các dòng được tích
        int viewRowCount = tbl_sanPhamCT.getRowCount();
        for (int viewRow = 0; viewRow < viewRowCount; viewRow++) {
            int modelRow = tbl_sanPhamCT.convertRowIndexToModel(viewRow);
            Object val = model.getValueAt(modelRow, modelColAction);
            boolean checked = (val instanceof Boolean) ? (Boolean) val : false;
            if (checked) {
                Object maObj = model.getValueAt(modelRow, modelColMa);
                if (maObj != null) {
                    maList.add(maObj.toString());
                }
            }
        }

        // 2. Nếu đã có dòng được tích -> trả về luôn
        if (!maList.isEmpty()) {
            return maList;
        }

        // 3. Không tích gì -> lấy tất cả mã trong bảng
        for (int viewRow = 0; viewRow < viewRowCount; viewRow++) {
            int modelRow = tbl_sanPhamCT.convertRowIndexToModel(viewRow);
            Object maObj = model.getValueAt(modelRow, modelColMa);
            if (maObj != null) {
                maList.add(maObj.toString());
            }
        }
        
        return maList;
    }
    
    public String formatCurrency(BigDecimal value) {
        DecimalFormatSymbols symbols = new DecimalFormatSymbols();
        symbols.setGroupingSeparator('.');
        DecimalFormat df = new DecimalFormat("#,###", symbols);
        df.setMaximumFractionDigits(0); // Không lấy phần thập phân
        df.setParseBigDecimal(true);
        return df.format(value) + " đ";
    }
    
    public BigDecimal parseCurrency(String text) throws ParseException {
        DecimalFormatSymbols symbols = new DecimalFormatSymbols();
        symbols.setGroupingSeparator('.');
        DecimalFormat df = new DecimalFormat("#,###", symbols);
        df.setParseBigDecimal(true);
        
        text = text.replace("đ", "").replace("Đ", "").trim();
        return (BigDecimal) df.parse(text);
    }
    
