        int idmausac = lstMauSacs.get(cbo_mausac.getSelectedIndex()).getIdmausac();
        int idxuatxu = lstXuatXus.get(cbo_xuatxu.getSelectedIndex()).getIdxuatxu();
        int idchatlieu = lstChatLieus.get(cbo_chatlieu.getSelectedIndex()).getIdchatlieu();
        int idloaidegiay = lstLoaiDeGiays.get(cbo_degiay.getSelectedIndex()).getIdloaidegiay();
        int idaygiay = lstDayGiays.get(cbo_daygiay.getSelectedIndex()).getIddaygiay();
        int idkieudang = lstKieuDangs.get(cbo_kieudang.getSelectedIndex()).getIdkieudang();
        String masanphamchitiet = this.maSPCT();
