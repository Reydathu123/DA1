    public void createTH() {
        if (!XDialog.confirm("Bạn có muốn thêm không")) {
            return;
        }
        thuonghieu h = this.getformTHuongHieu();
        if (txt_name.getText().trim().isEmpty()) {
            txt_name.requestFocus();
            XDialog.alert("Tên Bị Trống");
            return;
        }
         // biểu thức chính quy
         String regex = "^[\\p{L}\\s]+$";
         if (!txt_name.getText().matches(regex)) {
            XDialog.alert("Tên kiểu dáng phải là chữ và không có kí tự đặc biệt");
            return ;
        }
          String input =txt_name.getText().trim().replaceAll("\\s+", "");// replaceAll dùng để gộp nhiều khoảng trắng thành 0
       
        for (thuonghieu k : THCN.findAll()) {
              String db= k.getTenthuonghieu().trim().replaceAll("\\s+", "");
            if (input.equalsIgnoreCase(db)) {
                XDialog.alert("Trùng Tên Thương Hiệu");
                txt_name.setText("");
                return;
            }
        }
        THCN.create(h);
        XDialog.alert("Thêm Thành Công");
    }

