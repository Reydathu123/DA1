    public void createKichCo() {
        if (!XDialog.confirm("Bạn có muốn thêm không")) {
            return;
        }
        
        if (txt_name.getText().trim().isEmpty()) {
            txt_name.requestFocus();
            XDialog.alert("Tên Bị Trống");
            return;
        }
          String input =txt_name.getText().trim().replaceAll("\\s+", "");// replaceAll dùng để gộp nhiều khoảng trắng thành 0
       

        for (kichco k : KCCN.findAll()) {
            String db= k.getTenkichco().trim().replaceAll("\\s+", "");
            if (input.equalsIgnoreCase(db)) {
                XDialog.alert("Trùng Tên size");
                txt_name.setText("");
                return;
            }
        }
        int SIZE_MIN = 36;
            int SIZE_MAX = 43;
             
            int sizeValue;
            // Kiểm tra dữ liệu có phải số không
            try {
                sizeValue = Integer.parseInt(txt_name.getText().trim().replaceAll("\\s+", ""));
            } catch (NumberFormatException e) {
                XDialog.alert("Tên size phải là số (ví dụ: 36, 37...)");
                txt_name.setText("");
                return ;
            }
            
            if (sizeValue < SIZE_MIN || sizeValue > SIZE_MAX) {
                XDialog.alert("Size không hợp lệ Size chỉ cho phép từ" + SIZE_MIN + "đến" + SIZE_MAX);
                 txt_name.setText("");
                return ;
            }
      
        kichco h = this.getformKichCo();
        KCCN.create(h);
        XDialog.alert("Thêm Thành Công");
    }

