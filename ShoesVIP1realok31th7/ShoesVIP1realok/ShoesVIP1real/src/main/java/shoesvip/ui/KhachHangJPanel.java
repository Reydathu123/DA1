    private boolean validateFormKhachHang() {

        if (txt_ten.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập họ và tên.");
            txt_ten.requestFocus();
            return false;
        }
        String sdt = txt_sdt.getText().trim();
        if (sdt.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập số điện thoại.");
            txt_sdt.requestFocus();
            return false;
        }
        if (!sdt.matches("^0\\d{9}$")) {
            JOptionPane.showMessageDialog(this, "Số điện thoại không hợp lệ. Ví dụ: 0901234567");
            txt_sdt.requestFocus();
            return false;
        }

        String email = txt_email.getText().trim();
        if (email.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Email không được để trống");
            txt_email.requestFocus();
            return false;
        }
        if (!email.isEmpty() && !email.matches("^[\\w.-]+@[\\w.-]+\\.[a-zA-Z]{2,}$")) {
            JOptionPane.showMessageDialog(this, "Email không đúng định dạng.");
            txt_email.requestFocus();
            return false;
        }
        String ngaySinhStr = txt_ngaysinh.getText().trim();
        if (ngaySinhStr.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập ngày sinh.");
            txt_ngaysinh.requestFocus();
            return false;
        }

        Date ngaySinh = parseDate(ngaySinhStr);
        if (ngaySinh == null) {
            JOptionPane.showMessageDialog(this, "Ngày sinh không đúng định dạng (dd/MM/yyyy).");
            txt_ngaysinh.requestFocus();
            return false;
        }
        if (ngaySinh.after(new Date())) {
            JOptionPane.showMessageDialog(this, "Ngày sinh không được lớn hơn ngày hiện tại.");
            txt_ngaysinh.requestFocus();
            return false;
        }
        if (cbo_diachi.getSelectedIndex() == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn địa chỉ.");
            cbo_diachi.requestFocus();
            return false;
        }

        return true;
    }

