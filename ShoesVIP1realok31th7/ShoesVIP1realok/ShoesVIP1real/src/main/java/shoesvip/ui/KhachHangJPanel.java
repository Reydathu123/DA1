    private void btn_addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_addActionPerformed
        // TODO add your handling code here:                                      
        try {
            if (!validateFormKhachHang()) {
                return;
            }
            String maKH = maKhachHangNgauNhien();

            txt_makh.repaint(); // ép giao diện cập nhật lại
            txt_makh.revalidate();

            int confirm = JOptionPane.showConfirmDialog(this,
                    "Bạn có chắc chắn muốn thêm khách hàng này?",
                    "Xác nhận thêm",
                    JOptionPane.YES_NO_OPTION);
            if (confirm != JOptionPane.YES_OPTION) {
                return; // Không thêm nếu người dùng chọn "No"
            }

            khachhang kh = readForm();
            kh.setMakhachhang(maKH);
            if (kh == null) {
                JOptionPane.showMessageDialog(this, "Vui lòng điền đầy đủ thông tin khách hàng.");
                return;
            }

            // ======================== CHECK TRÙNG ========================
            List<khachhang> listKH = khDAO.findAll();
            boolean isDuplicate = listKH.stream().anyMatch(e
                    -> e.getSodienthoai().equalsIgnoreCase(kh.getSodienthoai())
                    || e.getEmail().equalsIgnoreCase(kh.getEmail())
            );
            if (isDuplicate) {
                JOptionPane.showMessageDialog(this, "Khách hàng đã tồn tại (trùng SĐT hoặc Email)!");
                return;
            }
            // ============================================================
            khDAO.create(kh);
            fillToTable();
            clearForm();
            JOptionPane.showMessageDialog(this, "Thêm khách hàng thành công!");
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Lỗi khi thêm khách hàng!\n" + e.getMessage());
        }
    }//GEN-LAST:event_btn_addActionPerformed

