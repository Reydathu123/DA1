    private void btn_timkiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_timkiemActionPerformed
        // TODO add your handling code here:
        String keyword = txt_timkiem.getText().trim(); // txtTim là ô nhập tìm kiếm
        if (keyword.isEmpty()) {
            fillToTable(); // hiển thị lại toàn bộ nếu rỗng
            return;
        }

        try {
            List<khachhang> list = khDAO.findByTen(keyword);
            DefaultTableModel model = (DefaultTableModel) tbl_thongtinkhachhang.getModel();
            model.setRowCount(0); // clear table
            int stt = 0;
            for (khachhang kh : list) {
                model.addRow(new Object[]{
                    ++stt,
                    kh.getMakhachhang(),
                    kh.getHovaten(),
                    kh.getNgaysinh(),
                    kh.getSodienthoai(),
                    kh.getEmail(),
                    kh.getGioitinh() == null ? "" : (kh.getGioitinh() ? "Nam" : "Nữ"),
                    kh.getIddiachi() == null ? "" : dckhDAO.findById(kh.getIddiachi()).getTinhthanh(),});
            }

            if (list.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Không tìm thấy khách hàng nào!");
            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Lỗi khi tìm kiếm khách hàng!");
        }
    }//GEN-LAST:event_btn_timkiemActionPerformed

    private void btn_suaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_suaActionPerformed
        // TODO add your handling code here:
        int row = tbl_thongtinkhachhang.getSelectedRow();

        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn khách hàng cần cập nhật.");
            return;
        }

        int confirm = JOptionPane.showConfirmDialog(this, "Bạn có chắc chắn muốn cập nhật thông tin khách hàng này?", "Xác nhận", JOptionPane.YES_NO_OPTION);
        if (confirm != JOptionPane.YES_OPTION) {
            return;
        }

        try {
            if (!validateFormKhachHang()) {
                return;
            }
            khachhang kh = listKH.get(row); // Danh sách này bạn đã fill từ khDAO.findAll()

            if (kh == null) {
                JOptionPane.showMessageDialog(this, "Không tìm thấy khách hàng.");
                return;
            }

            khachhang khMoi = readForm();
            khMoi.setIdkhachhang(kh.getIdkhachhang()); // Giữ nguyên ID cũ
            khDAO.update(khMoi); // Cập nhật thông tin

            fillToTable();
            clearForm();

            JOptionPane.showMessageDialog(this, "Cập nhật khách hàng thành công!");
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Lỗi khi cập nhật khách hàng!");
        }
    }//GEN-LAST:event_btn_suaActionPerformed

