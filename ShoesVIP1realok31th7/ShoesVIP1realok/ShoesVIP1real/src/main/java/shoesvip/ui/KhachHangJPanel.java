    public void importKhachHangFromExcel(File file) {

        try (FileInputStream fis = new FileInputStream(file); XSSFWorkbook workbook = new XSSFWorkbook(fis)) {
            XSSFSheet sheet = workbook.getSheetAt(0);
            DefaultTableModel model = (DefaultTableModel) tbl_thongtinkhachhang.getModel();
            model.setRowCount(0); // Xóa dữ liệu cũ
            DataFormatter formatter = new DataFormatter();

            // Lấy toàn bộ địa chỉ từ DB để map
            List<diachikhachhang> listDiaChi = dckhDAO.findAll();

            int count = 0;
            for (int i = 1; i <= sheet.getLastRowNum(); i++) {
                Row row = sheet.getRow(i);
                if (row != null) {
                    try {

                        String tenKH = formatter.formatCellValue(row.getCell(0));
                        String ngaySinhStr = formatter.formatCellValue(row.getCell(1));
                        String sdt = formatter.formatCellValue(row.getCell(2));
                        String email = formatter.formatCellValue(row.getCell(3));
                        String gioiTinhStr = formatter.formatCellValue(row.getCell(4));
                        String diaChiExcel = formatter.formatCellValue(row.getCell(5));

                        // Parse ngày sinh
                        Date ngaySinh = ngaySinhStr.isEmpty() ? null : sdf.parse(ngaySinhStr);
                        boolean gioiTinh = gioiTinhStr.equalsIgnoreCase("Nam");

                        // Map địa chỉ từ Excel sang id
                        int idDiaChi = 0;
                        for (diachikhachhang dc : listDiaChi) {
                            String fullDC = dc.getDiachicuthe() + ", " + dc.getPhuongxa() + ", " + dc.getQuanhuyen() + ", " + dc.getTinhthanh();
                            if (fullDC.equalsIgnoreCase(diaChiExcel.trim())) {
                                idDiaChi = dc.getIddiachi(); // hoặc dc.getIddiachi()
                                break;
                            }
                        }
                        String[] parts = diaChiExcel.split(",");
                        if (idDiaChi == 0) {
                            dckhDAO.create(new diachikhachhang(maDCKhachHangNgauNhien(), parts[0].trim(), parts[1].trim(), parts[2].trim(), parts[3].trim(), null, null, null, null, XDate.now(), null, null, true));

                        }
                        listDiaChi = dckhDAO.findAll();
                        idDiaChi = listDiaChi.get(listDiaChi.size() - 1).getIddiachi();
                        // Tạo khách hàng
                        khachhang kh = new khachhang();
                        kh.setMakhachhang(maKhachHangNgauNhien());
                        kh.setHovaten(tenKH);
                        kh.setNgaysinh(ngaySinh);
                        kh.setSodienthoai(sdt);
                        kh.setEmail(email);
                        kh.setGioitinh(gioiTinh);
                        kh.setIddiachi(idDiaChi);

                        // Lưu DB
                        khDAO.create(kh);

                        // Hiển thị bảng
                        loadTable();
                        count++;
                    } catch (Exception rowEx) {
                        System.err.println("Lỗi dòng " + (i + 1) + ": " + rowEx.getMessage());
                    }
                }
            }

            JOptionPane.showMessageDialog(null, "Đã import và lưu " + count + " khách hàng vào hệ thống!");
        } catch (Exception ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(null, "Lỗi khi đọc file Excel: " + ex.getMessage());
        }

    }

